def times_by_10(x):
	"""
    Floyd's first function.
    Args:
        x (float): Some number.
    Returns:
        float: Returns :math:`y=10x`
    """

	return 10*x

def plus_two(x):
	"""
    Floyd's second function.
    Args:
        x (float): Some number.
    Returns:
        float: Returns :math:`y=x+2`
    """

	return x+2


