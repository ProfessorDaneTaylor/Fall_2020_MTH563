import numpy as np


def cube_numbers(x):
    """
    The first function.
    
    Args:
        x (float): Some number.
    Returns:
        float: Returns :math:`y=x^3`
    
    """
    return x**3


def ten_times_greater(x):
    """
    The second function.
    
    Args:
        x (float): Some number.
    Returns:
        float: Returns :math:`y=x*10`
        
    """
    return x*10





